# 성별, 키를 입력받아 남자일 경우 (m) 172.5 이상이면 평균이상, 이하면 평균이하로 
# 여자일 경우(f) 159.6 이상이명 평균이상, 이하면 평균이하로 출력
gender = input("성별을 '남 또는 여'로 입력하세요 >>")
height = float(input("신장을 '숫자만' 입력하세요 >>"))

if gender == "남" :
    if height > 172.5 :
        print("당신의 키는 평균 이상입니다.")
    else :
        print("당신의 키는 평균 이하입니다.")
elif gender == "여" :
    if height > 159.6 :
        print("당신의 키는 평균 이상입니다.")
    else :
        print("당신의 키는 평균 이하입니다.")
else :
    print("입력 오류입니다. 다시 작성하시기 바랍니다.")